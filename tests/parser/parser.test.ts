describe('pdf-object-parser', () => {
    function parse(src: string): PdfValue {
        const tok = createTokenizer(textEncoder(src));
        return parseValue(tok);
    }

    describe('primitives', () => {
        it('parses null', () => {
            expect(parse('null')).toBeNull();
        });

        it('parses booleans', () => {
            expect(parse('true')).toBe(true);
            expect(parse('false')).toBe(false);
        });

        it('parses integers', () => {
            expect(parse('42')).toBe(42);
            expect(parse('-7')).toBe(-7);
        });

        it('parses real numbers', () => {
            expect(parse('3.14')).toBeCloseTo(3.14);
        });

        it('parses names', () => {
            const val = parse('/Type');
            expect(isName(val)).toBe(true);
            expect((val as PdfName).value).toBe('Type');
        });

        it('parses strings', () => {
            expect(parse('(Hello)')).toBe('Hello');
        });
    });

    describe('indirect references', () => {
        it('parses "N G R" as indirect ref', () => {
            const val = parse('5 0 R');
            expect(isRef(val)).toBe(true);
            const ref = val as PdfRef;
            expect(ref.num).toBe(5);
            expect(ref.gen).toBe(0);
        });

        it('does not confuse two numbers with non-R', () => {
            const tok = createTokenizer(textEncoder('5 0'));
            const val = parseValue(tok);
            expect(val).toBe(5);
        });
    });

    describe('arrays', () => {
        it('parses empty array', () => {
            const val = parse('[ ]');
            expect(isArray(val)).toBe(true);
            expect(val).toHaveLength(0);
        });

        it('parses array with mixed types', () => {
            const val = parse('[1 /Name (string) true null]');
            expect(isArray(val)).toBe(true);
            const arr = val as PdfValue[];
            expect(arr).toEqual([1, { type: 'name', value: 'Name' }, 'string', true, null]);
        });

        it('parses nested arrays', () => {
            const val = parse('[[1 2] [3 4]]');
            expect(isArray(val)).toBe(true);
            const arr = val as PdfValue[];
            expect(arr).toHaveLength(2);
            expect(arr[0]).toEqual([1, 2]);
        });

        it('parses array with refs', () => {
            const val = parse('[1 0 R 2 0 R]');
            expect(isArray(val)).toBe(true);
            const arr = val as PdfValue[];
            expect(arr).toHaveLength(2);
            expect(isRef(arr[0])).toBe(true);
            expect((arr[0] as PdfRef).num).toBe(1);
        });
    });

    describe('dictionaries', () => {
        it('parses empty dict', () => {
            const val = parse('<< >>');
            expect(isDict(val)).toBe(true);
            expect((val as PdfDict).size).toBe(0);
        });

        it('parses dict with entries', () => {
            const val = parse('<< /Type /Catalog /Pages 3 0 R >>');
            expect(isDict(val)).toBe(true);
            const dict = val as PdfDict;
            expect(dictGetName(dict, 'Type')).toBe('Catalog');
            expect(isRef(dict.get('Pages')!)).toBe(true);
        });

        it('parses nested dicts', () => {
            const val = parse('<< /Font << /F1 5 0 R >> >>');
            expect(isDict(val)).toBe(true);
            const dict = val as PdfDict;
            const font = dictGetDict(dict, 'Font');
            expect(font).toBeDefined();
            expect(isRef(font!.get('F1')!)).toBe(true);
        });

        it('parses dict with array values', () => {
            const val = parse('<< /Kids [1 0 R 2 0 R] /Count 2 >>');
            expect(isDict(val)).toBe(true);
            const dict = val as PdfDict;
            const kids = dictGetArray(dict, 'Kids');
            expect(kids).toHaveLength(2);
            expect(dictGetNum(dict, 'Count')).toBe(2);
        });
    });

    describe('indirect objects', () => {
        it('parses indirect object definition', () => {
            const tok = createTokenizer(textEncoder('1 0 obj\n<< /Type /Catalog >>\nendobj'));
            const obj = parseIndirectObject(tok);
            expect(obj).not.toBeNull();
            expect(obj!.num).toBe(1);
            expect(obj!.gen).toBe(0);
            expect(isDict(obj!.value)).toBe(true);
        });

        it('parses indirect object with number value', () => {
            const tok = createTokenizer(textEncoder('7 0 obj\n42\nendobj'));
            const obj = parseIndirectObject(tok);
            expect(obj!.num).toBe(7);
            expect(obj!.value).toBe(42);
        });

        it('parses indirect object with string value', () => {
            const tok = createTokenizer(textEncoder('3 0 obj\n(Hello World)\nendobj'));
            const obj = parseIndirectObject(tok);
            expect(obj!.value).toBe('Hello World');
        });
    });

    describe('streams', () => {
        it('parses stream object with /Length', () => {
            const content = 'BT /F1 12 Tf (Hello) Tj ET';
            const src = `<< /Length ${content.length} >>\nstream\n${content}\nendstream`;
            const tok = createTokenizer(textEncoder(src));
            const val = parseValue(tok);
            expect(isStream(val)).toBe(true);
            const stream = val as PdfStream;
            expect(stream.data.length).toBe(content.length);
        });
    });

    describe('dict helpers', () => {
        it('dictGet returns undefined for missing keys', () => {
            const dict: PdfDict = new Map();
            expect(dictGet(dict, 'Missing')).toBeUndefined();
        });

        it('dictGetRef returns PdfRef or undefined', () => {
            const dict: PdfDict = new Map();
            dict.set('Ref', { type: 'ref' as const, num: 1, gen: 0 });
            dict.set('Num', 42);
            expect(dictGetRef(dict, 'Ref')).toEqual({ type: 'ref', num: 1, gen: 0 });
            expect(dictGetRef(dict, 'Num')).toBeUndefined();
        });
    });

    describe('isRef/isStream/isDict/isArray guards', () => {
        it('isRef correctly identifies refs', () => {
            expect(isRef({ type: 'ref', num: 1, gen: 0 })).toBe(true);
            expect(isRef(null)).toBe(false);
            expect(isRef(42)).toBe(false);
            expect(isRef('string')).toBe(false);
        });

        it('isStream correctly identifies streams', () => {
            expect(isStream({ type: 'stream', dict: new Map(), data: new Uint8Array() })).toBe(true);
            expect(isStream(null)).toBe(false);
        });

        it('isDict correctly identifies Map dictionaries', () => {
            expect(isDict(new Map())).toBe(true);
            expect(isDict({} as unknown as PdfValue)).toBe(false);
            expect(isDict(null)).toBe(false);
        });

        it('isArray correctly identifies arrays', () => {
            expect(isArray([1, 2, 3])).toBe(true);
            expect(isArray(null)).toBe(false);
            expect(isArray('not array')).toBe(false);
        });
    });
});

