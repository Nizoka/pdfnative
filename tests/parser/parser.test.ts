describe('pdf-modifier', () => {
    let originalBytes: Uint8Array;
    let reader: PdfReader;

    beforeAll(() => {
        originalBytes = buildPDFBytes(minimalTableParams(), { compress: false });
        reader = openPdf(originalBytes);
    });

    it('creates a modifier from reader', () => {
        const mod = createModifier(reader);
        expect(mod.reader).toBe(reader);
    });

    it('save returns original when no modifications', () => {
        const mod = createModifier(reader);
        const saved = mod.save();
        expect(saved).toBe(originalBytes); // Same reference, no copy
    });

    it('addObject allocates a new object number', () => {
        const mod = createModifier(reader);
        const num = mod.addObject(42);
        expect(num).toBeGreaterThan(0);
        expect(mod.getObject(num)).toBe(42);
    });

    it('setObject overrides an existing object', () => {
        const mod = createModifier(reader);
        const originalVal = mod.getObject(1);
        mod.setObject(1, 'replaced');
        expect(mod.getObject(1)).toBe('replaced');
        expect(originalVal).not.toBe('replaced');
    });

    it('save with modifications produces valid PDF', () => {
        const mod = createModifier(reader);

        // Add a new info dict entry
        const newDict: PdfDict = new Map();
        newDict.set('Type', 'Custom');
        newDict.set('Value', 42);
        mod.addObject(newDict);

        const saved = mod.save();

        // Modified should be larger
        expect(saved.length).toBeGreaterThan(originalBytes.length);

        // Should start with original content
        for (let i = 0; i < originalBytes.length; i++) {
            expect(saved[i]).toBe(originalBytes[i]);
        }

        // Should contain new xref and trailer
        const tail = new TextDecoder('latin1').decode(saved.subarray(originalBytes.length));
        expect(tail).toContain('xref');
        expect(tail).toContain('trailer');
        expect(tail).toContain('startxref');
        expect(tail).toContain('%%EOF');
        expect(tail).toContain('/Prev');
    });

    it('modified PDF is re-parseable', () => {
        const mod = createModifier(reader);
        const newDict: PdfDict = new Map();
        newDict.set('Custom', 'test-value');
        const objNum = mod.addObject(newDict);

        const saved = mod.save();
        const reader2 = openPdf(saved);

        // Should still have the same pages
        expect(reader2.pageCount).toBe(reader.pageCount);

        // Should be able to resolve the new object
        const obj = reader2.getObject(objNum);
        expect(isDict(obj)).toBe(true);
        if (isDict(obj)) {
            expect(obj.get('Custom')).toBe('test-value');
        }
    });

    it('nextObjNum tracks correctly', () => {
        const mod = createModifier(reader);
        const start = mod.nextObjNum;
        mod.addObject('obj1');
        expect(mod.nextObjNum).toBe(start + 1);
        mod.addObject('obj2');
        expect(mod.nextObjNum).toBe(start + 2);
    });

    it('getObject returns original for unmodified objects', () => {
        const mod = createModifier(reader);
        const val = mod.getObject(1); // catalog
        expect(val).not.toBeNull();
        expect(isDict(val)).toBe(true);
    });

    it('getObject returns null for non-existent objects', () => {
        const mod = createModifier(reader);
        expect(mod.getObject(99999)).toBeNull();
    });

    it('modified PDF round-trips through multiple modifications', () => {
        const mod1 = createModifier(reader);
        mod1.addObject('first-mod');
        const saved1 = mod1.save();

        const reader2 = openPdf(saved1);
        const mod2 = createModifier(reader2);
        mod2.addObject('second-mod');
        const saved2 = mod2.save();

        const reader3 = openPdf(saved2);
        expect(reader3.pageCount).toBe(reader.pageCount);
    });
});

