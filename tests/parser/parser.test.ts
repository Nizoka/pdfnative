describe('pdf-tokenizer', () => {
    describe('numbers', () => {
        it('parses integers', () => {
            const tokens = collectTokens('0 42 -7 12345');
            expect(tokens.map(t => t.value)).toEqual([0, 42, -7, 12345]);
            expect(tokens.every(t => t.type === 'number')).toBe(true);
        });

        it('parses real numbers', () => {
            const tokens = collectTokens('3.14 -0.5 .25 100.');
            expect(tokens.map(t => t.value)).toEqual([3.14, -0.5, 0.25, 100]);
        });
    });

    describe('strings', () => {
        it('parses literal strings', () => {
            const tokens = collectTokens('(Hello World)');
            expect(tokens).toHaveLength(1);
            expect(tokens[0].type).toBe('string');
            expect(tokens[0].value).toBe('Hello World');
        });

        it('handles escape sequences', () => {
            const tokens = collectTokens('(line1\\nline2)');
            expect(tokens[0].value).toBe('line1\nline2');
        });

        it('handles nested parentheses', () => {
            const tokens = collectTokens('(text (nested) more)');
            expect(tokens[0].value).toBe('text (nested) more');
        });

        it('handles escaped parentheses', () => {
            const tokens = collectTokens('(escaped \\( paren \\))');
            expect(tokens[0].value).toBe('escaped ( paren )');
        });

        it('handles octal escapes', () => {
            const tokens = collectTokens('(\\101)'); // 'A' = 0o101 = 65
            expect(tokens[0].value).toBe('A');
        });

        it('parses hex strings', () => {
            const tokens = collectTokens('<48656C6C6F>');
            expect(tokens[0].type).toBe('string');
            expect(tokens[0].value).toBe('Hello');
        });

        it('handles odd-length hex strings (padding)', () => {
            const tokens = collectTokens('<ABC>');
            expect(tokens[0].type).toBe('string');
            // 0xAB, 0xC0
            expect(tokens[0].value).toBe('\xAB\xC0');
        });
    });

    describe('names', () => {
        it('parses simple names', () => {
            const tokens = collectTokens('/Type /Catalog /Pages');
            expect(tokens.map(t => t.value)).toEqual(['Type', 'Catalog', 'Pages']);
            expect(tokens.every(t => t.type === 'name')).toBe(true);
        });

        it('handles #XX escapes in names', () => {
            const tokens = collectTokens('/A#20B');
            expect(tokens[0].value).toBe('A B');
        });
    });

    describe('keywords', () => {
        it('parses boolean and null keywords', () => {
            const tokens = collectTokens('true false null');
            expect(tokens.map(t => ({ type: t.type, value: t.value }))).toEqual([
                { type: 'keyword', value: 'true' },
                { type: 'keyword', value: 'false' },
                { type: 'keyword', value: 'null' },
            ]);
        });

        it('parses obj/endobj keywords', () => {
            const tokens = collectTokens('obj endobj');
            expect(tokens.map(t => t.value)).toEqual(['obj', 'endobj']);
        });

        it('parses stream/endstream keywords', () => {
            const tokens = collectTokens('stream endstream');
            expect(tokens.map(t => t.value)).toEqual(['stream', 'endstream']);
        });

        it('parses xref and trailer keywords', () => {
            const tokens = collectTokens('xref trailer startxref');
            expect(tokens.map(t => t.value)).toEqual(['xref', 'trailer', 'startxref']);
        });
    });

    describe('delimiters', () => {
        it('parses array delimiters', () => {
            const tokens = collectTokens('[ 1 2 3 ]');
            expect(tokens[0].type).toBe('arrayOpen');
            expect(tokens[4].type).toBe('arrayClose');
        });

        it('parses dictionary delimiters', () => {
            const tokens = collectTokens('<< /Type /Catalog >>');
            expect(tokens[0].type).toBe('dictOpen');
            expect(tokens[3].type).toBe('dictClose');
        });
    });

    describe('comments', () => {
        it('skips comments', () => {
            const tokens = collectTokens('42 %this is a comment\n99');
            expect(tokens.map(t => t.value)).toEqual([42, 99]);
        });
    });

    describe('whitespace', () => {
        it('handles various whitespace characters', () => {
            const tokens = collectTokens('1\t2\r\n3');
            expect(tokens.map(t => t.value)).toEqual([1, 2, 3]);
        });
    });

    describe('peek', () => {
        it('peek does not consume token', () => {
            const tok = createTokenizer(textEncoder('42 99'));
            const p = tok.peek();
            expect(p?.value).toBe(42);
            const t = tok.next();
            expect(t?.value).toBe(42);
        });
    });
});

