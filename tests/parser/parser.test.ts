function collectTokens(src: string): PdfToken[] {
    const tok = createTokenizer(textEncoder(src));
    const tokens: PdfToken[] = [];
    let t: PdfToken | null;
    while ((t = tok.next()) !== null) tokens.push(t);
    return tokens;
}

