describe('pdf-inflate', () => {
    it('round-trips with deflateSync', () => {
        const original = textEncoder('Hello, PDF inflate round-trip test!');
        const compressed = deflateSync(original);
        const decompressed = inflateSync(compressed);
        expect(decompressed).toEqual(original);
    });

    it('round-trips empty data', () => {
        const original = new Uint8Array(0);
        const compressed = deflateSync(original);
        const decompressed = inflateSync(compressed);
        expect(decompressed.length).toBe(0);
    });

    it('round-trips large data', () => {
        const original = new Uint8Array(10000);
        for (let i = 0; i < original.length; i++) original[i] = i & 0xFF;
        const compressed = deflateSync(original);
        const decompressed = inflateSync(compressed);
        expect(decompressed).toEqual(original);
    });

    it('round-trips repetitive data (good compression)', () => {
        const original = new Uint8Array(5000);
        for (let i = 0; i < original.length; i++) original[i] = 0x41; // 'A'
        const compressed = deflateSync(original);
        expect(compressed.length).toBeLessThan(original.length);
        const decompressed = inflateSync(compressed);
        expect(decompressed).toEqual(original);
    });

    it('works with pure JS implementation (setInflateImpl)', async () => {
        // Temporarily disable native
        const original = textEncoder('Pure JS inflate test');
        const compressed = deflateSync(original);

        setInflateImpl(null);
        const decompressed = inflateSync(compressed);
        expect(decompressed).toEqual(original);

        // Restore native
        await initNodeDecompression_parser();
    });

    it('throws on invalid data', () => {
        expect(() => inflateSync(new Uint8Array([0, 1, 2, 3]))).toThrow();
    });
});

