describe('Compressed PDF â€” Table Builder', () => {
    it('should contain /Filter /FlateDecode in streams when compress=true', () => {
        const pdf = buildPDF(makeParams(), { compress: true });
        expect(pdf).toContain('/Filter /FlateDecode');
    });

    it('should NOT contain /Filter /FlateDecode when compress=false', () => {
        const pdf = buildPDF(makeParams(), { compress: false });
        expect(pdf).not.toContain('/Filter /FlateDecode');
    });

    it('should NOT contain /Filter /FlateDecode by default (backward compat)', () => {
        const pdf = buildPDF(makeParams());
        expect(pdf).not.toContain('/Filter /FlateDecode');
    });

    it('should produce valid PDF header and trailer when compressed', () => {
        const pdf = buildPDF(makeParams(), { compress: true });
        expect(pdf.startsWith('%PDF-1.4')).toBe(true);
        expect(pdf).toMatch(/%%EOF$/);
        expect(pdf).toContain('xref');
        expect(pdf).toContain('startxref');
    });

    it('should produce smaller output when compressed (Latin)', () => {
        const rows = Array.from({ length: 50 }, (_, i) => ({
            cells: [`2024-01-${String(i + 1).padStart(2, '0')}`, `Transaction ${i + 1}`, '100.00', '', '900.00'],
            type: i % 2 === 0 ? 'credit' : 'debit',
            pointed: false,
        }));
        const params = makeParams({ rows });
        const uncompressed = buildPDFBytes(params, { compress: false });
        const compressed = buildPDFBytes(params, { compress: true });
        expect(compressed.length).toBeLessThan(uncompressed.length);
    });

    it('should produce valid xref offsets when compressed', () => {
        const pdf = buildPDF(makeParams(), { compress: true });
        // Parse xref section
        const xrefIdx = pdf.indexOf('xref\n');
        expect(xrefIdx).toBeGreaterThan(0);
        // Verify startxref points to xref
        const startxrefMatch = pdf.match(/startxref\n(\d+)\n/);
        expect(startxrefMatch).not.toBeNull();
        const startxref = Number(startxrefMatch![1]);
        expect(startxref).toBe(xrefIdx);
    });

    it('should contain catalog and page objects when compressed', () => {
        const pdf = buildPDF(makeParams(), { compress: true });
        expect(pdf).toContain('/Type /Catalog');
        expect(pdf).toContain('/Type /Pages');
        expect(pdf).toContain('/Type /Page');
    });

    it('compressed streams should decompress to valid content', () => {
        const pdf = buildPDF(makeParams(), { compress: true });
        // Extract a compressed stream between "stream\n" and "\nendstream"
        const streamRegex = /\/Filter \/FlateDecode \/Length (\d+).*?>>\nstream\n/g;
        const match = streamRegex.exec(pdf);
        expect(match).not.toBeNull();
        const streamStart = match!.index + match![0].length;
        const streamLen = Number(match![1]);
        const streamData = pdf.slice(streamStart, streamStart + streamLen);
        // Convert binary string to Uint8Array
        const bytes = new Uint8Array(streamData.length);
        for (let i = 0; i < streamData.length; i++) {
            bytes[i] = streamData.charCodeAt(i) & 0xFF;
        }
        // Should decompress without error
        const decompressed = zlibInflateSync(bytes);
        expect(decompressed.length).toBeGreaterThan(0);
    });
});

