describe('Compressed + Encrypted PDF', () => {
    it('should support compress + encryption together (compressâ†’encrypt order)', () => {
        const pdf = buildPDF(makeParams(), {
            compress: true,
            encryption: {
                ownerPassword: 'testowner',
                algorithm: 'aes128',
            },
        });
        expect(pdf.startsWith('%PDF-1.4')).toBe(true);
        expect(pdf).toContain('/Encrypt');
        // FlateDecode should be in the dict entries (before encryption scrambles stream data)
        expect(pdf).toContain('/Filter /FlateDecode');
    });

    it('should produce valid structure with AES-256 + compression', () => {
        const pdf = buildPDF(makeParams(), {
            compress: true,
            encryption: {
                ownerPassword: 'test256',
                algorithm: 'aes256',
            },
        });
        expect(pdf).toContain('/Encrypt');
        expect(pdf).toContain('/Filter /FlateDecode');
        expect(pdf).toMatch(/%%EOF$/);
    });

    it('document builder should support compress + encryption', () => {
        const pdf = buildDocumentPDF(makeDocParams(), {
            compress: true,
            encryption: {
                ownerPassword: 'docowner',
                algorithm: 'aes128',
            },
        });
        expect(pdf).toContain('/Filter /FlateDecode');
        expect(pdf).toContain('/Encrypt');
    });
});

describe('Compressed + Tagged PDF', () => {
    it('should support compress + tagged mode together', () => {
        const pdf = buildPDF(makeParams(), {
            compress: true,
            tagged: true,
        });
        expect(pdf).toContain('/Filter /FlateDecode');
        expect(pdf).toContain('/MarkInfo << /Marked true >>');
        expect(pdf).toContain('/StructTreeRoot');
    });

    it('should NOT compress XMP metadata stream in tagged mode', () => {
        const pdf = buildPDF(makeParams(), {
            compress: true,
            tagged: true,
        });
        // XMP metadata should contain /Type /Metadata without FlateDecode
        const xmpMatch = pdf.match(/\/Type \/Metadata \/Subtype \/XML \/Length \d+/);
        expect(xmpMatch).not.toBeNull();
        // Verify the XMP stream dict does NOT have FlateDecode
        expect(xmpMatch![0]).not.toContain('FlateDecode');
    });

    it('should compress ICC profile stream in tagged mode', () => {
        const pdf = buildPDF(makeParams(), {
            compress: true,
            tagged: true,
        });
        // ICC profile stream should have /N 3 and FlateDecode
        expect(pdf).toMatch(/\/Filter \/FlateDecode \/Length \d+.*?\/N 3|\/N 3.*?\/Filter \/FlateDecode/);
    });

    it('document builder should support compress + tagged', () => {
        const pdf = buildDocumentPDF(makeDocParams(), {
            compress: true,
            tagged: true,
        });
        expect(pdf).toContain('/Filter /FlateDecode');
        expect(pdf).toContain('/MarkInfo << /Marked true >>');
    });
});

describe('Compression size reduction benchmarks', () => {
    it('should reduce 100-row Latin table by >30%', () => {
        const rows = Array.from({ length: 100 }, (_, i) => ({
            cells: [`2024-01-${String((i % 28) + 1).padStart(2, '0')}`, `Transaction ${i + 1}`, `${(i * 10).toFixed(2)}`, '', `${(10000 - i * 10).toFixed(2)}`],
            type: i % 2 === 0 ? 'credit' : 'debit',
            pointed: i === 0,
        }));
        const params = makeParams({ rows });
        const uncompressed = buildPDFBytes(params);
        const compressed = buildPDFBytes(params, { compress: true });
        const ratio = 1 - compressed.length / uncompressed.length;
        expect(ratio).toBeGreaterThan(0.3);
    });
});
