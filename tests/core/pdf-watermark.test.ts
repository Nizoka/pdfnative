        const result = buildDocumentPDF(makeDocParams(), {
            tagged: true,
            watermark: { text: { text: 'DRAFT', opacity: 1.0 } },
        });
        expect(result).toMatch(/^%PDF-/);
        expect(result).toContain('DRAFT');
        // Watermark should NOT be in structure tree (not accessible content)
