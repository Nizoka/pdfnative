describe('buildPDF input validation', () => {
    it('should throw when params is null', () => {
        expect(() => buildPDF(null as unknown as PdfParams)).toThrow('params is required');
    });

    it('should throw when params is undefined', () => {
        expect(() => buildPDF(undefined as unknown as PdfParams)).toThrow('params is required');
    });

    it('should throw when rows is not an array', () => {
        expect(() => buildPDF({ ...makeMinimalParams(), rows: 'not-array' } as unknown as PdfParams)).toThrow('params.rows must be an array');
    });

    it('should throw when headers is not an array', () => {
        expect(() => buildPDF({ ...makeMinimalParams(), headers: null } as unknown as PdfParams)).toThrow('params.headers must be an array');
    });

    it('should throw when row count exceeds safe limit', () => {
        const rows = Array.from({ length: 100_001 }, () => ({
            cells: ['x'], type: 'credit', pointed: false,
        }));
        expect(() => buildPDF(makeMinimalParams({ rows }))).toThrow('exceeds safe limit');
    });

    it('should accept 100,000 rows without throwing', () => {
        const rows = Array.from({ length: 100_000 }, () => ({
            cells: ['x', 'y', 'z'], type: 'credit', pointed: false,
        }));
        // Should not throw (may take a moment)
        const result = buildPDF(makeMinimalParams({ rows }));
        expect(result).toMatch(/^%PDF-1\.4/);
    });
});

describe('buildPDF /Info dictionary', () => {
    it('should contain /Info reference in trailer', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toMatch(/\/Info \d+ 0 R/);
    });

    it('should emit /Producer (pdfnative)', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('/Producer (pdfnative)');
    });

    it('should emit /CreationDate in PDF date format', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toMatch(/\/CreationDate \(D:\d{14}\)/);
    });

    it('should emit /Title from visible title when docTitle is absent', () => {
        const result = buildPDF(makeMinimalParams({ title: 'My Report' }));
        expect(result).toContain('/Title (My Report)');
    });

    it('should use docTitle over title when both present', () => {
        const result = buildPDF(makeMinimalParams({ title: 'Visible', docTitle: 'Metadata Title' }));
        expect(result).toContain('/Title (Metadata Title)');
    });

    it('should escape special characters in title', () => {
        const result = buildPDF(makeMinimalParams({ docTitle: 'Report (Q1) 2026\\final' }));
        expect(result).toContain('/Title (Report \\(Q1\\) 2026\\\\final)');
    });

    it('should have valid xref offset for Info object', () => {
        const result = buildPDF(makeMinimalParams());
        // Find the Info object reference in trailer
        const infoMatch = result.match(/\/Info (\d+) 0 R/);
        expect(infoMatch).not.toBeNull();
        const infoObjNum = parseInt(infoMatch![1], 10);
        // Verify the object content
        expect(result).toContain(`${infoObjNum} 0 obj`);
        expect(result).toContain('/Producer (pdfnative)');
    });
});

describe('buildPDF Unicode/CIDFont path', () => {
    function makeUnicodeParams(overrides?: Partial<PdfParams>): PdfParams {
        const fontData = makeMockFontData();
        const fontEntries: FontEntry[] = [{ fontData, fontRef: '/F3', lang: 'test' }];
        return {
            title: 'Unicode Report',
            infoItems: [{ label: 'Period', value: '2026' }],
            balanceText: 'Balance: 100',
            countText: '1 item',
            headers: ['Col A', 'Col B', 'Col C'],
            rows: [
                { cells: ['A', 'B', '10'], type: 'credit', pointed: false },
                { cells: ['B', 'A', '01'], type: 'debit', pointed: true },
            ],
            footerText: 'Footer',
            fontEntries,
            ...overrides,
        };
    }

    it('should produce valid PDF with CIDFont objects', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toMatch(/^%PDF-1\.4/);
        expect(result).toMatch(/%%EOF$/);
        expect(result).toContain('/Subtype /CIDFontType2');
    });

    it('should emit Type0 composite font', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toContain('/Subtype /Type0');
        expect(result).toContain('/Encoding /Identity-H');
    });

    it('should emit FontDescriptor', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toContain('/Type /FontDescriptor');
        expect(result).toContain('/FontFile2');
    });

    it('should emit ToUnicode CMap stream', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toContain('beginbfchar');
        expect(result).toContain('endbfchar');
        expect(result).toContain('endcmap');
    });

    it('should emit /W array in CIDFont', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toMatch(/\/W \[.+\]/);
    });

    it('should include hex-encoded glyph data in content streams', () => {
        const result = buildPDF(makeUnicodeParams());
        // GID 1 for 'A' = hex 0001
        expect(result).toContain('<0001>');
    });

    it('should emit Helvetica fallback fonts alongside CIDFont', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toContain('/BaseFont /Helvetica');
        expect(result).toContain('/BaseFont /Helvetica-Bold');
    });

    it('should have valid xref offsets in Unicode mode', () => {
        const result = buildPDF(makeUnicodeParams());
        const xrefMatch = result.match(/xref\n0 (\d+)\n([\s\S]+?)trailer/);
        expect(xrefMatch).not.toBeNull();
        const entries = xrefMatch![2].trim().split('\n');
        expect(entries[0]).toMatch(/0000000000 65535 f/);
        for (let i = 1; i < entries.length; i++) {
            const offsetMatch = entries[i].match(/^(\d{10}) 00000 n/);
            expect(offsetMatch).not.toBeNull();
            const offset = parseInt(offsetMatch![1], 10);
            expect(result.substring(offset, offset + `${i} 0 obj`.length)).toBe(`${i} 0 obj`);
        }
    });

    it('should handle pagination in Unicode mode', () => {
        const rows = Array.from({ length: 80 }, (_, i) => ({
            cells: ['A', 'B', `${i}`],
            type: 'credit',
            pointed: false,
        }));
        const result = buildPDF(makeUnicodeParams({ rows }));
        const pageCount = (result.match(/\/Type \/Page /g) || []).length;
        expect(pageCount).toBeGreaterThan(1);
    });

    it('should emit /Info dictionary in Unicode mode', () => {
        const result = buildPDF(makeUnicodeParams());
        expect(result).toContain('/Producer (pdfnative)');
        expect(result).toMatch(/\/Info \d+ 0 R/);
    });

    it('should support fontData shorthand (single font)', () => {
        const fontData = makeMockFontData();
        const result = buildPDF(makeMinimalParams({ fontData }));
        expect(result).toContain('/Subtype /CIDFontType2');
        expect(result).toContain('/Encoding /Identity-H');
    });

    // ── PDF/A-3 with attachments ─────────────────────────────────
    it('should produce PDF/A-3b with embedded file attachments', () => {
        const xmlData = new TextEncoder().encode('<invoice><total>100</total></invoice>');
        const result = buildPDF(makeMinimalParams(), {
            tagged: 'pdfa3b',
            attachments: [{
                filename: 'invoice.xml',
                data: xmlData,
                mimeType: 'application/xml',
                relationship: 'Data',
            }],
        });
        expect(result).toContain('%PDF-1.7');
        expect(result).toContain('pdfaid:part>3');
        expect(result).toContain('/AF [');
        expect(result).toContain('/EmbeddedFiles');
        expect(result).toContain('/AFRelationship /Data');
        expect(result).toContain('/Type /Filespec');
        expect(result).toContain('/Type /EmbeddedFile');
        expect(result).toContain('(invoice.xml)');
    });

    it('should produce PDF/A-3b without attachments', () => {
        const result = buildPDF(makeMinimalParams(), { tagged: 'pdfa3b' });
        expect(result).toContain('%PDF-1.7');
        expect(result).toContain('pdfaid:part>3');
        expect(result).not.toContain('/AF [');
    });

    it('should throw when attachments used without pdfa3b', () => {
        const att = [{
            filename: 'test.xml',
            data: new TextEncoder().encode('<x/>'),
            mimeType: 'application/xml',
        }];
        expect(() => buildPDF(makeMinimalParams(), { tagged: true, attachments: att })).toThrow('pdfa3b');
        expect(() => buildPDF(makeMinimalParams(), { attachments: att })).toThrow('pdfa3b');
    });
});
