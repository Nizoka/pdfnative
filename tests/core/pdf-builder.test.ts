import { describe, it, expect } from 'vitest';
import { buildPDF, buildPDFBytes } from '../../src/core/pdf-builder.js';
import type { PdfParams, FontData, FontEntry } from '../../src/types/pdf-types.js';

/** Minimal mock FontData for Unicode path testing. */
function makeMockFontData(): FontData {
    return {
        metrics: { unitsPerEm: 1000, numGlyphs: 10, defaultWidth: 500, ascent: 800, descent: -200, bbox: [0, -200, 600, 800], capHeight: 700, stemV: 50 },
        fontName: 'TestFont',
        cmap: { 65: 1, 66: 2, 32: 3, 48: 4, 49: 5 },
        defaultWidth: 500,
        widths: { 1: 600, 2: 700, 3: 250, 4: 500, 5: 500 },
        pdfWidthArray: '1 [600] 2 [700] 3 [250] 4 [500] 5 [500]',
        ttfBase64: 'AAAAAAAAAA==', // 7 zero bytes — invalid TTF, subsetter will return original
        gsub: {},
        markAnchors: null,
        mark2mark: null,
    };
}

function makeMinimalParams(overrides?: Partial<PdfParams>): PdfParams {
    return {
        title: 'Test Report',
        infoItems: [{ label: 'Period', value: 'Jan 2026' }],
        balanceText: 'Balance: $100.00',
        countText: '2 items',
        headers: ['Date', 'Description', 'Amount'],
        rows: [
            { cells: ['01/01', 'Item A', '$50.00'], type: 'credit', pointed: false },
            { cells: ['01/02', 'Item B', '-$25.00'], type: 'debit', pointed: true },
        ],
        footerText: 'Generated by pdfnative',
        ...overrides,
    };
}

describe('buildPDF', () => {
    it('should produce a valid PDF string starting with %PDF-1.4', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toMatch(/^%PDF-1\.4/);
    });

    it('should end with %%EOF', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toMatch(/%%EOF$/);
    });

    it('should contain binary header marker', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('%\xE2\xE3\xCF\xD3');
    });

    it('should contain catalog object', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('/Type /Catalog');
        expect(result).toContain('/Pages 2 0 R');
    });

    it('should contain pages object', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('/Type /Pages');
        expect(result).toContain('/Count 1');
    });

    it('should contain Helvetica font references', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('/BaseFont /Helvetica');
        expect(result).toContain('/BaseFont /Helvetica-Bold');
        expect(result).toContain('/Encoding /WinAnsiEncoding');
    });

    it('should contain page content with title', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('Test Report');
    });

    it('should contain xref table', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('xref\n');
        expect(result).toContain('startxref\n');
    });

    it('should contain trailer with /Root and /Size', () => {
        const result = buildPDF(makeMinimalParams());
        expect(result).toContain('trailer');
        expect(result).toContain('/Root 1 0 R');
        expect(result).toMatch(/\/Size \d+/);
    });

    it('should have valid xref offsets pointing to obj declarations', () => {
        const result = buildPDF(makeMinimalParams());
        const xrefMatch = result.match(/xref\n0 (\d+)\n([\s\S]+?)trailer/);
        expect(xrefMatch).not.toBeNull();

        const entries = xrefMatch![2].trim().split('\n');
        // First entry is the free entry (0000000000 65535 f)
        expect(entries[0]).toMatch(/0000000000 65535 f/);

        // Each subsequent entry should point to a valid "N 0 obj" in the PDF
        for (let i = 1; i < entries.length; i++) {
            const offsetMatch = entries[i].match(/^(\d{10}) 00000 n/);
            expect(offsetMatch).not.toBeNull();
            const offset = parseInt(offsetMatch![1], 10);
            const objMarker = `${i} 0 obj`;
            expect(result.substring(offset, offset + objMarker.length)).toBe(objMarker);
        }
    });
});

// ── Header / Footer Templates ────────────────────────────────────────

describe('Header/Footer Templates (Table Builder)', () => {
    it('should render custom footer template text', () => {
        const result = buildPDF(makeMinimalParams(), {
            footerTemplate: { left: 'Custom Footer', right: 'Page {page} of {pages}' },
        });
        expect(result).toContain('Custom Footer');
        expect(result).toContain('Page 1 of 1');
    });

    it('should render footer center text', () => {
        const result = buildPDF(makeMinimalParams(), {
            footerTemplate: { center: 'Centered' },
        });
        expect(result).toContain('Centered');
    });

    it('should resolve {title} placeholder in footer', () => {
        const result = buildPDF(makeMinimalParams({ title: 'My Report' }), {
            footerTemplate: { left: '{title}' },
        });
        expect(result).toContain('My Report');
    });

    it('should resolve {date} placeholder', () => {
        const result = buildPDF(makeMinimalParams(), {
            footerTemplate: { left: '{date}' },
        });
        expect(result).toMatch(/\d{4}-\d{2}-\d{2}/);
    });

    it('should override footerText when footerTemplate is provided', () => {
        const result = buildPDF(
            makeMinimalParams({ footerText: 'Old Footer' }),
            { footerTemplate: { left: 'New Footer' } },
        );
        expect(result).toContain('New Footer');
    });

    it('should use footerText as backward compat when no template', () => {
        const result = buildPDF(makeMinimalParams({ footerText: 'Legacy Footer' }));
        expect(result).toContain('Legacy Footer');
        expect(result).toContain('1/1');
    });

    it('should render header template on each page', () => {
        // Create enough rows to span 2 pages
        const rows = Array.from({ length: 80 }, (_, i) => ({
            cells: [`${i}`, 'Description', 'Cat', '$10', ''],
            type: 'credit' as const,
            pointed: false,
        }));
        const result = buildPDF(makeMinimalParams({ rows }), {
            headerTemplate: { left: 'Header Text', right: '{page}/{pages}' },
        });
        const matches = result.match(/Header Text/g);
        expect(matches).not.toBeNull();
        expect(matches!.length).toBeGreaterThanOrEqual(2);
    });

    it('should render header with custom color', () => {
        const result = buildPDF(makeMinimalParams(), {
            headerTemplate: { left: 'Red Header', color: '#FF0000' },
        });
        expect(result).toContain('Red Header');
        expect(result).toContain('1 0 0 rg');
    });

    it('should produce valid PDF with header + footer templates', () => {
        const result = buildPDF(makeMinimalParams(), {
            headerTemplate: { left: 'Company', center: '{title}', right: '{date}' },
            footerTemplate: { left: 'Confidential', right: 'Page {page}/{pages}' },
        });
        expect(result).toMatch(/^%PDF-/);
        expect(result).toMatch(/%%EOF$/);
        expect(result).toContain('xref');
    });

    it('should handle header + footer + tagged mode', () => {
        const result = buildPDF(makeMinimalParams(), {
            tagged: true,
            headerTemplate: { left: 'Header' },
            footerTemplate: { right: '{page}/{pages}' },
        });
        expect(result).toContain('Header');
        expect(result).toContain('1/1');
        expect(result).toContain('/StructTreeRoot');
    });

    it('should handle empty template (no header/footer content)', () => {
        const result = buildPDF(makeMinimalParams(), { footerTemplate: {} });
        expect(result).toMatch(/^%PDF-/);
        expect(result).toMatch(/%%EOF$/);
    });

    it('should adjust pagination when header reduces available height', () => {
        const rows = Array.from({ length: 60 }, (_, i) => ({
            cells: [`${i}`, 'Desc', 'Cat', '$10', ''],
            type: 'credit' as const,
            pointed: false,
        }));
        const resultNoHeader = buildPDF(makeMinimalParams({ rows }));
        const resultWithHeader = buildPDF(makeMinimalParams({ rows }), {
            headerTemplate: { left: 'Header' },
        });
        const noHeaderPages = (resultNoHeader.match(/\/Type \/Page /g) ?? []).length;
        const withHeaderPages = (resultWithHeader.match(/\/Type \/Page /g) ?? []).length;
        expect(withHeaderPages).toBeGreaterThanOrEqual(noHeaderPages);
    });
});

describe('buildPDFBytes', () => {
    it('should return Uint8Array', () => {
        const result = buildPDFBytes(makeMinimalParams());
        expect(result).toBeInstanceOf(Uint8Array);
    });

    it('should start with %PDF-1.4 bytes', () => {
        const result = buildPDFBytes(makeMinimalParams());
        const header = String.fromCharCode(...result.slice(0, 9));
        expect(header).toBe('%PDF-1.4\n');
    });

    it('should have consistent length with string version', () => {
        const params = makeMinimalParams();
        const str = buildPDF(params);
        const bytes = buildPDFBytes(params);
        expect(bytes.length).toBe(str.length);
    });
});

describe('buildPDF pagination', () => {
    it('should create multiple pages for many rows', () => {
        const rows = Array.from({ length: 200 }, (_, i) => ({
            cells: [`${i}`, `Item ${i}`, `$${i}.00`],
            type: i % 2 === 0 ? 'credit' : 'debit',
            pointed: i % 5 === 0,
        }));
        const result = buildPDF(makeMinimalParams({ rows }));
        // Should have more than 1 page — count /Type /Page with space, not /Pages
        const pageCount = (result.match(/\/Type \/Page /g) || []).length;
        expect(pageCount).toBeGreaterThan(1);
    });

    it('should handle empty rows', () => {
        const result = buildPDF(makeMinimalParams({ rows: [] }));
        expect(result).toMatch(/^%PDF-1\.4/);
        expect(result).toMatch(/%%EOF$/);
    });

    it('should handle single row', () => {
        const result = buildPDF(makeMinimalParams({
            rows: [{ cells: ['01/01', 'Single', '$1.00'], type: 'credit', pointed: false }],
        }));
        expect(result).toContain('/Count 1');
    });
});

